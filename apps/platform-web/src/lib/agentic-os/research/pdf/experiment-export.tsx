/**
 * Truncate a string to N characters at the nearest word boundary,
 * appending an ellipsis when truncated. Pure, exported for tests.
 */
export function truncateForPdf(text: string, max: number): string {
  if (typeof text !== 'string') return '';
  const tidy = text.trim();
  if (tidy.length <= max) return tidy;
  const slice = tidy.slice(0, max);
  const lastSpace = slice.lastIndexOf(' ');
  const cut = lastSpace > Math.floor(max * 0.6) ? slice.slice(0, lastSpace) : slice;
  return `${cut.trimEnd()}…`;
}

