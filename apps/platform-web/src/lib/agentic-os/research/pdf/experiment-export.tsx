function safeDate(value: string | null): string {
  if (!value) return '—';
  return value.slice(0, 10);
}

// ─── Reference grouping helper (pure, exported for tests) ────────────────

export function groupReferencesByRelevance(
  refs: ExperimentPdfReferenceRow[],
): Array<{ relevance: ReferenceRelevance; rows: ExperimentPdfReferenceRow[] }> {
  const order: ReferenceRelevance[] = [
    'cites',
    'methods',
    'prior_art',
    'contradicts',
    'builds_on',
  ];
  const groups = new Map<ReferenceRelevance, ExperimentPdfReferenceRow[]>();
  for (const rel of order) groups.set(rel, []);
  for (const r of refs) {
    const arr = groups.get(r.relevance);
    if (arr) arr.push(r);
  }
  return order
    .map((relevance) => ({ relevance, rows: groups.get(relevance) ?? [] }))
    .filter((g) => g.rows.length > 0);
}

// ─── Component ───────────────────────────────────────────────────────────

export function ExperimentExportPdf({
  header,
  counts,
  notebook,
  hypotheses,
  references,
  datasets,
  protocols,
  generatedAt,
}: ExperimentExportPdfProps): React.ReactElement {
  const description = truncateForPdf(header.description ?? '', 800);

  const metaFields = [
    { label: 'Status', value: EXPERIMENT_STATUS_LABELS[header.status] ?? header.status },
    { label: 'Target', value: safeDate(header.targetCompletionDate) },
    { label: 'Hypotheses', value: String(counts.hypotheses) },
    { label: 'Notebook', value: String(counts.notebookEntries) },
    { label: 'Papers', value: String(counts.papers) },
    { label: 'Datasets', value: String(counts.datasets) },
    { label: 'Protocols', value: String(counts.protocols) },
  ];

  const datasetColumns: PdfTableColumnDef<ExperimentPdfDatasetRow>[] = [
    { header: 'Name', width: 0.28, render: (r) => r.name },
    { header: 'Kind', width: 0.12, render: (r) => DATASET_KIND_LABELS[r.kind] ?? r.kind },
    { header: 'Version', width: 0.1, render: (r) => r.version ?? '—' },
    { header: 'URL', width: 0.28, render: (r) => r.url },
    { header: 'Archived', width: 0.08, render: (r) => (r.archived ? 'yes' : 'no') },
    { header: 'Checksum', width: 0.14, render: (r) => truncateForPdf(r.checksum ?? '—', 18) },
  ];

  const groupedReferences = groupReferencesByRelevance(references);

  return (
    <Document>
      {/* ─── Page 1: cover + counts + description + tags ─────────── */}
      <Page size="LETTER" style={PdfPageStyles.page}>
        <PdfHeader
          title="Experiment export"
          subtitle="Pantheon Research OS"
          generatedAt={generatedAt}
        />

        <View style={{ flexDirection: 'row', alignItems: 'center', marginBottom: 8 }}>
          <Text style={styles.titleH1}>{header.title}</Text>
          <Text style={styles.statusBadge}>
            {EXPERIMENT_STATUS_LABELS[header.status] ?? header.status}
          </Text>
        </View>

        {description ? (
          <Text style={styles.description}>{description}</Text>
        ) : null}

        <PdfMetadataBlock fields={metaFields} />

        {header.tags.length > 0 ? (
          <View style={styles.tagRow}>
            {header.tags.map((t) => (
              <Text key={`tag-${t}`} style={styles.tagChip}>
                {t}
              </Text>
            ))}
          </View>
        ) : null}

        <PdfFooter projectName={FOOTER_TEXT} />
      </Page>

      {/* ─── Page 2+: Notebook timeline ───────────────────────────── */}
      <Page size="LETTER" style={PdfPageStyles.page}>
        <PdfHeader
          title="Notebook timeline"
          subtitle={header.title}
          projectName="Pantheon Research OS"
          generatedAt={generatedAt}
        />

        <Text style={styles.sectionTitle}>
          Last {notebook.length} {notebook.length === 1 ? 'entry' : 'entries'}
        </Text>

        {notebook.length === 0 ? (
          <Text style={styles.empty}>No notebook entries yet.</Text>
        ) : (
          notebook.map((entry) => (
            <View key={`nb-${entry.id}`} style={styles.cardWrapper} wrap={false}>
              <View style={styles.cardHeader}>
                <Text style={styles.cardTitle}>{entry.title || 'Untitled entry'}</Text>
                <Text style={styles.cardKindChip}>{entry.entryKind}</Text>
              </View>
              <Text style={styles.cardMeta}>{safeDate(entry.entryAt)}</Text>
              {entry.bodyMd ? (
                <Text style={styles.cardBody}>{truncateForPdf(entry.bodyMd, 400)}</Text>
              ) : (
                <Text style={styles.empty}>No body.</Text>
              )}
            </View>
          ))
        )}

        <PdfFooter projectName={FOOTER_TEXT} />
      </Page>

      {/* ─── Hypotheses + References + Datasets + Protocols ──────── */}
      <Page size="LETTER" style={PdfPageStyles.page}>
        <PdfHeader
          title="Reproducibility packet"
          subtitle={header.title}
          projectName="Pantheon Research OS"
          generatedAt={generatedAt}
        />

        {/* ─── Hypotheses ─────────────────────────────────────── */}
        <Text style={styles.sectionTitle}>Hypotheses</Text>
        {hypotheses.length === 0 ? (
          <Text style={styles.empty}>No hypotheses linked.</Text>
        ) : (
          hypotheses.map((h) => (
            <View key={`hyp-${h.id}`} style={styles.cardWrapper} wrap={false}>
              <View style={styles.cardHeader}>
                <Text style={styles.cardTitle}>{h.title}</Text>
              </View>
              {h.statement ? (
                <Text style={styles.hypothesisStatement}>
                  {truncateForPdf(h.statement, 400)}
                </Text>
              ) : null}
              <Text style={styles.cardMeta}>
                Predictions: {h.predictionCount} · Falsifiers: {h.falsifierCount}
              </Text>
              {h.topPredictions.length > 0 ? (
                <View>
                  <Text style={styles.groupHeading}>Predictions</Text>
                  {h.topPredictions.map((p, i) => (
                    <Text key={`hyp-${h.id}-p-${i}`} style={styles.hypothesisSub}>
                      • {truncateForPdf(p, 220)}
                    </Text>
                  ))}
                </View>
              ) : null}
              {h.topFalsifiers.length > 0 ? (
                <View>
                  <Text style={styles.groupHeading}>Falsifiers</Text>
                  {h.topFalsifiers.map((f, i) => (
                    <Text key={`hyp-${h.id}-f-${i}`} style={styles.hypothesisSub}>
                      • {truncateForPdf(f, 220)}
                    </Text>
                  ))}
                </View>
              ) : null}
            </View>
          ))
        )}

        {/* ─── References (grouped by relevance) ─────────────── */}
        <Text style={styles.sectionTitle}>References</Text>
        {references.length === 0 ? (
          <Text style={styles.empty}>No papers cited.</Text>
        ) : (
          groupedReferences.map((g) => (
            <View key={`refgroup-${g.relevance}`} wrap={false}>
              <Text style={styles.groupHeading}>
                {REFERENCE_RELEVANCE_LABELS[g.relevance]} ({g.rows.length})
              </Text>
              {g.rows.map((r, i) => (
                <Text
                  key={`ref-${g.relevance}-${i}`}
                  style={styles.hypothesisSub}
                >
                  {r.paperTitle}
                  {r.authors ? ` — ${r.authors}` : ''}
                  {r.venueYear ? ` (${r.venueYear})` : ''}
                  {r.identifier ? ` · ${r.identifier}` : ''}
                </Text>
              ))}
            </View>
          ))
        )}

        {/* ─── Datasets ───────────────────────────────────────── */}
        <Text style={styles.sectionTitle}>Datasets</Text>
        {datasets.length === 0 ? (
          <Text style={styles.empty}>No datasets attached.</Text>
        ) : (
          <PdfTable columns={datasetColumns} rows={datasets} />
        )}

        {/* ─── Protocols ──────────────────────────────────────── */}
        <Text style={styles.sectionTitle}>Protocols (pinned)</Text>
        {protocols.length === 0 ? (
          <Text style={styles.empty}>No protocols pinned.</Text>
        ) : (
          protocols.map((p) => (
            <View key={`proto-${p.id}`} style={styles.cardWrapper} wrap={false}>
              <View style={styles.cardHeader}>
                <Text style={styles.cardTitle}>{p.title}</Text>
                <Text style={styles.cardKindChip}>
                  {PROTOCOL_KIND_LABELS[p.kind] ?? p.kind}
                </Text>
              </View>
              <Text style={styles.cardMeta}>Pinned v{p.pinnedVersion}</Text>
              {p.bodyMd ? (
                <Text style={styles.cardBody}>{truncateForPdf(p.bodyMd, 400)}</Text>
              ) : (
                <Text style={styles.empty}>No body.</Text>
              )}
            </View>
          ))
        )}

        <PdfFooter projectName={FOOTER_TEXT} />
      </Page>
    </Document>
  );
}
