function shortMemoryId(memoryId: string): string {
  const tidy = memoryId.replace(/-/g, '');
  return `M-${tidy.slice(0, 6)}`;
}

function superscript(n: number): string {
  if (n < 1) return '';
  const map: Record<number, string> = {
    1: '¹',
    2: '²',
    3: '³',
    4: '⁴',
    5: '⁵',
    6: '⁶',
    7: '⁷',
    8: '⁸',
    9: '⁹',
  };
  return map[n] ?? `[${n}]`;
}

export function BookExportPdf({
  book,
  authorName,
  chapters,
  memories,
  provenance,
  generatedAt,
}: BookExportPdfProps): React.ReactElement {
  const memoryById = new Map<string, BookPdfMemoryLookup>();
  for (const m of memories) memoryById.set(m.id, m);

  // Title page word count = sum of latest-revision word counts.
  const totalWords = chapters.reduce(
    (acc, c) => acc + (c.latest?.wordCount ?? 0),
    0,
  );

  const titleMeta = [
    { label: 'Status', value: book.status },
    { label: 'Chapters', value: String(chapters.length) },
    { label: 'Word count', value: totalWords.toLocaleString() },
    {
      label: 'Audience',
      value: book.targetAudience ?? '—',
    },
    {
      label: 'Target',
      value: book.targetCompletionDate ?? '—',
    },
    {
      label: 'Author',
      value: authorName ?? '—',
    },
  ];

  return (
    <Document>
      {/* ─── Title page ───────────────────────────────────────── */}
      <Page size="LETTER" style={PdfPageStyles.page}>
        <PdfHeader
          title="Book export"
          subtitle="Pantheon Autobiographer OS"
          generatedAt={generatedAt}
        />
        <Text style={styles.titleH1}>{book.title}</Text>
        {book.subtitle ? (
          <Text style={styles.titleSub}>{book.subtitle}</Text>
        ) : null}
        {book.description ? (
          <Text style={styles.description}>{book.description}</Text>
        ) : null}
        <PdfMetadataBlock fields={titleMeta} />
        <PdfFooter projectName={FOOTER_TEXT} />
      </Page>

      {/* ─── Chapters ─────────────────────────────────────────── */}
      {chapters.map((chapter) => {
        const latest = chapter.latest;
        const paragraphs = latest ? splitParagraphs(latest.bodyText) : [];
        const citations = latest?.citations ?? [];
        const citationsByParagraph = new Map<number, RevisionCitation>();
        for (const c of citations) citationsByParagraph.set(c.paragraphIndex, c);

        // Per-chapter footnote ordering — restart at each chapter so
        // the markers in the body align with the per-chapter footnote
        // list at the bottom of the chapter page.
        const footnoteOrder: string[] = [];
        const footnoteIndex = new Map<string, number>();
        for (const c of citations) {
          for (const memoryId of c.memoryIds) {
            if (!footnoteIndex.has(memoryId)) {
              footnoteOrder.push(memoryId);
              footnoteIndex.set(memoryId, footnoteOrder.length);
            }
          }
        }

        const chapterTitle = chapter.title ?? 'Untitled chapter';
        const positionLabel = `Ch ${String(chapter.position + 1).padStart(2, '0')}`;
        return (
          <Page
            key={`chapter-${chapter.id}`}
            size="LETTER"
            style={PdfPageStyles.page}
          >
            <PdfHeader
              title={chapterTitle}
              subtitle={`${book.title} — ${positionLabel}`}
              projectName="Pantheon Autobiographer OS"
              generatedAt={generatedAt}
            />

            <View
              style={{ flexDirection: 'row', alignItems: 'center', marginBottom: 4 }}
            >
              <Text style={styles.chapterMeta}>
                {latest
                  ? `Revision v${latest.version} by ${
                      latest.author === 'coach' ? 'coach' : 'user'
                    } · ${latest.wordCount.toLocaleString()} words · ${paragraphs.length} ${paragraphs.length === 1 ? 'paragraph' : 'paragraphs'}`
                  : 'No revisions yet'}
              </Text>
              <Text style={styles.statusBadge}>
                {CHAPTER_STATUS_LABELS[chapter.status]}
              </Text>
            </View>

            {chapter.summary ? (
              <Text style={styles.summary}>{chapter.summary}</Text>
            ) : null}

            {paragraphs.length === 0 ? (
              <Text style={styles.empty}>
                This chapter has no prose yet.
              </Text>
            ) : (
              paragraphs.map((p, i) => {
                const cite = citationsByParagraph.get(i);
                const markers = cite
                  ? cite.memoryIds
                      .map((mid) => footnoteIndex.get(mid))
                      .filter((n): n is number => typeof n === 'number')
                      .map((n) => superscript(n))
                      .join('')
                  : '';
                return (
                  <Text key={`p-${chapter.id}-${i}`} style={styles.paragraph}>
                    {p}
                    {markers ? ' ' : ''}
                    {markers}
                  </Text>
                );
              })
            )}

            {footnoteOrder.length > 0 ? (
              <View wrap={false}>
                <Text
                  style={{
                    fontSize: 8,
                    color: '#475569',
                    textTransform: 'uppercase',
                    marginTop: 12,
                    marginBottom: 4,
                  }}
                >
                  Source memories
                </Text>
                {footnoteOrder.map((mid, i) => {
                  const memory = memoryById.get(mid);
                  const title = memory?.title ?? 'memory not available';
                  const era = memory?.whenInLife ? ` (${memory.whenInLife})` : '';
                  return (
                    <Text key={`fn-${chapter.id}-${mid}`} style={styles.footnote}>
                      {superscript(i + 1)} from memory {shortMemoryId(mid)} ‘{title}’
                      {era}
                    </Text>
                  );
                })}
              </View>
            ) : null}

            <PdfFooter projectName={FOOTER_TEXT} />
          </Page>
        );
      })}

      {/* ─── Provenance appendix ─────────────────────────────── */}
      <Page size="LETTER" style={PdfPageStyles.page}>
        <PdfHeader
          title="Provenance appendix"
          subtitle={book.title}
          projectName="Pantheon Autobiographer OS"
          generatedAt={generatedAt}
        />
        <Text style={styles.appendixHeading}>Cited memories</Text>
        {provenance.length === 0 ? (
          <Text style={styles.empty}>No memories cited yet.</Text>
        ) : (
          provenance.map((row) => {
            const refs = row.chapterReferences
              .map((c) => {
                const label = c.chapterTitle ?? c.chapterSlug ?? `chapter-${c.position + 1}`;
                return `Ch ${String(c.position + 1).padStart(2, '0')} ‘${label}’`;
              })
              .join(', ');
            const era = row.memoryWhenInLife ? ` (${row.memoryWhenInLife})` : '';
            return (
              <Text key={`prov-${row.memoryId}`} style={styles.appendixRow}>
                {shortMemoryId(row.memoryId)} ‘{row.memoryTitle}’{era} → {refs}
              </Text>
            );
          })
        )}
        <PdfFooter projectName={FOOTER_TEXT} />
      </Page>
    </Document>
  );
}
