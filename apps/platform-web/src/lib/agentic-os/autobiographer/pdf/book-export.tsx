/**
 * Autobiographer OS — Book Export PDF template.
 *
 * Renders a full book: title-page meta, then each chapter's latest
 * revision body with paragraph-level footnotes, then a provenance
 * appendix listing every cited memory with the chapters that cite it.
 *
 * Phase 5 seam — the chapter loop iterates whatever ordering the route
 * passes in. Phase 4 routes default to `position`; Phase 5 will compute
 * an arc-ordered chapter id list and pass it instead. The function
 * signature stays the same.
 *
 * @license MIT — Tiresias Autobiographer OS Phase 4 (internal).
 */

import * as React from 'react';
import {
  Document,
  Page,
  View,
  Text,
  PdfHeader,
  PdfFooter,
  PdfMetadataBlock,
  PdfPageStyles,
  StyleSheet,
} from '../../_shared/pdf/primitives';
import {
  CHAPTER_STATUS_LABELS,
  type ChapterStatus,
} from '../chapters';
import { splitParagraphs } from './chapter-export';
import type { RevisionCitation } from '../chapter-revisions';

const FOOTER_TEXT =
  'Generated by Pantheon Autobiographer OS — provenance preserved';

export interface BookPdfBook {
  title: string;
  subtitle?: string | null;
  description?: string | null;
  status: string;
  targetCompletionDate?: string | null;
  targetAudience?: string | null;
}

export interface BookPdfChapter {
  id: string;
  title: string | null;
  slug: string | null;
  position: number;
  status: ChapterStatus;
  summary: string | null;
  /** Latest revision attached to this chapter, or null if no revisions. */
  latest: {
    version: number;
    author: 'user' | 'coach';
    bodyText: string;
    wordCount: number;
    citations: RevisionCitation[];
  } | null;
}

export interface BookPdfMemoryLookup {
  id: string;
  title: string | null;
  whenInLife: string | null;
}

export interface BookPdfProvenanceRow {
  memoryId: string;
  memoryTitle: string;
  memoryWhenInLife: string | null;
  chapterReferences: Array<{
    chapterId: string;
    chapterTitle: string | null;
    chapterSlug: string | null;
    position: number;
  }>;
}

export interface BookExportPdfProps {
  book: BookPdfBook;
  authorName?: string | null;
  chapters: BookPdfChapter[];
  memories: BookPdfMemoryLookup[];
  provenance: BookPdfProvenanceRow[];
  generatedAt?: Date;
}

